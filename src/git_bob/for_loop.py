"""
for_loop.py

This module demonstrates the use of a for-loop to output numbers from 0 to 10.

"""

def print_numbers():
    """Print numbers from 0 to 10.

    Outputs each number in the range [0, 10] to the console.
    """
    for number in range(11):
        print(number)

if __name__ == "__main__":
    print_numbers()
