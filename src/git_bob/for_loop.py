def print_numbers():
    """Prints numbers from 0 to 10.

    Demonstrates a simple for-loop in Python that iterates over a range
    of numbers between 0 and 10, inclusive. Each number is printed on
    a new line.

    Returns
    -------
    None
    """
    for number in range(11):
        print(number)

if __name__ == "__main__":
    print_numbers()
