"""
for_loop.py

This module demonstrates the use of a for-loop to output numbers between 0 and 10.
"""

def output_numbers():
    """Outputs numbers from 0 to 10.

    This function uses a for-loop to iterate through the range and prints each number.
    """
    for number in range(11):
        print(number)

if __name__ == "__main__":
    output_numbers()
