"""
for_loop.py

This module contains a demonstration of a for-loop in Python.
"""

def print_numbers():
    """
    Print numbers from 0 to 10.

    Outputs:
        Prints each number in the range from 0 to 10 inclusive.
    """
    for number in range(11):
        print(number)

if __name__ == "__main__":
    print_numbers()
