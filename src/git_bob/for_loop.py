def print_numbers():
    """Prints numbers from 0 to 10.

    This function demonstrates the use of a for-loop in Python
    to iterate over a range of numbers and print them.

    Returns
    -------
    None
    """
    for number in range(11):
        print(number)

if __name__ == "__main__":
    print_numbers()
