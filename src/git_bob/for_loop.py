"""
for_loop.py

This module demonstrates the use of a for-loop to output numbers
between 0 and 10.

Example usage:
    >> output_numbers()
"""

def output_numbers():
    """Outputs numbers from 0 to 10.

    Returns
    -------
    None
    """
    for i in range(11):
        print(i)

if __name__ == "__main__":
    output_numbers()
