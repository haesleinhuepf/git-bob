def print_numbers():
    """Prints numbers between 0 and 10.

    This function uses a for-loop to iterate through the range
    from 0 to 10 (inclusive) and prints each number.
    """
    for number in range(11):
        print(number)

if __name__ == "__main__":
    print_numbers()
