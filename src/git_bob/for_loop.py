# src/git_bob/for_loop.py

def print_numbers():
    """
    Print numbers from 0 to 10.

    This function demonstrates the use of a for-loop to iterate
    over a range of numbers and print each one to the console.
    """
    for number in range(11):
        print(number)

# Call the function
if __name__ == "__main__":
    print_numbers()
