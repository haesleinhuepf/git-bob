# src/git_bob/for_loop.py

def print_numbers():
    """Prints numbers from 0 to 10.

    This function demonstrates a for-loop that iterates
    through the range of numbers from 0 to 10 and prints
    each number.
    """
    for number in range(11):
        print(number)

if __name__ == "__main__":
    print_numbers()
