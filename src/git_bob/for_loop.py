def print_numbers():
    """Prints numbers from 0 to 10.

    This function uses a for-loop to iterate through a range of numbers
    and prints each number to the console.

    Returns
    -------
    None
    """
    for number in range(11):
        print(number)

if __name__ == "__main__":
    print_numbers()
