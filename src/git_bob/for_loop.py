"""
for_loop.py

This module demonstrates the use of for-loops in Python.
"""

def print_numbers():
    """
    Print numbers from 0 to 10.

    This function outputs the numbers between 0 and 10 inclusive,
    using a for-loop.
    """
    for i in range(11):
        print(i)

if __name__ == "__main__":
    print_numbers()
