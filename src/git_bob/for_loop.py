def print_numbers():
    """Print numbers from 0 to 10.

    This function demonstrates a simple for-loop that iterates 
    through the range of numbers from 0 to 10, inclusive, 
    and prints each number.
    """
    for num in range(11):  # Range goes from 0 to 10 inclusive
        print(num)

if __name__ == "__main__":
    print_numbers()
